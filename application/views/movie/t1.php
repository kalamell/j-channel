					<p>ข้อมูลเกี่ยวกับภาพยนตร์</p>
					<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ฉลาดเกมส์โกง (อังกฤษ: Bad Genius) เป็นภาพยนตร์ไทยที่ออกฉายในปี พ.ศ. 2560 แนวดราม่า-ระทึกขวัญ กำกับโดยนัฐวุฒิ พูนพิริยะ ผู้มีชื่อเสียงจากผลงาน เคาท์ดาวน์ นำแสดงโดย ชุติมณฑน์ จึงเจริญสุขยิ่ง, ชานน สันตินธรกุล, อิษยา ฮอสุวรรณ และ ธีรดนย์ ศุภพันธ์ภิญโญ เล่าเรื่องของนักเรียนชั้นมัธยมปลายกลุ่มหนึ่งที่คิดหารายได้จากการโกงข้อสอบ ภาพยนตร์เรื่องนี้เข้าฉายในประเทศไทยอย่างเป็นทางการเมื่อวันที่ 3 พฤษภาคม พ.ศ. 2560 และได้รับเลือกให้เป็นภาพยนตร์ฉายเปิดเทศกาลภาพยนตร์เอเชียนิวยอร์ก (New York Asian Film Festival - NYAFF) ครั้งที่ 16 ณ สหรัฐอเมริกา เมื่อวันที่ 30 มิถุนายนของปีเดียวกัน ทั้งนี้ฉลาดเกมส์โกง ยังเป็นภาพยนตร์ไทยเรื่องแรกที่ถูกนำไปรีมาสเตอร์เพื่อเข้าฉายในโรงภาพยนตร์ไอแมกซ์อีกด้วย แต่ในด้านเสียงถูกรีมาสเตอร์ เฉพาะภาษาจีนเท่านั้นภาพยนตร์เรื่องนี้เป็นภาพยนตร์ไทยที่ทำรายได้สูงสุดในต่างประเทศ ทำลายสถิติภาพยนตร์ไทยเรื่อง องค์บาก ที่ทำรายได้ในต่างประเทศ 515 ล้านบาท และชัตเตอร์ กดติดวิญญาณ ที่ทำรายได้ในต่างประเทศ 231 ล้านบาท เมื่อปี พ.ศ. 2547</p>
					<p>เรื่องย่อ</p>
					<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ทุกครั้งที่ฝนดินสอลงกระดาษสอบ ล้วนมีความหมาย ลิน (ชุติมณฑน์ จึงเจริญสุขยิ่ง) นักเรียนสายเรียนดี เจ้าของ เกรดเฉลี่ย 4.00 ทุกปีการศึกษา แต่ด้วยการที่เธอมักจะต้องช่วยบอกข้อสอบให้กับเพื่อนซี้อย่าง เกรซ (อิษยา ฮอสุวรรณ) นักเรียนสายกิจกรรมแต่ผลการเรียนกลับสวนทางและ พัฒน์ (ธีรดนย์ ศุภพันธุ์ภิญโญ) เด็กที่เติบโตมาในครอบครัวที่ร่ำรวยและมีแนวคิดว่าเงินซื้อทุกอย่างได้แม้กระทั่งคำตอบของข้อสอบ ทำให้ ลิน ฉุกคิดธุรกิจให้ลอกข้อสอบนี้ขึ้นมาได้ และสร้างรายได้ให้เธออย่างเป็นกอบเป็นกำ และเมื่อการสอบสุดท้าทาย อย่าง STIC ใกล้เข้ามาถึง นักเรียนหลายร้อยคนต่างคาดหวังจะนำคะแนนสอบเหล่านี้เปิดทางเข้าสู่เหล่ามหาลัยชั้นนำ ลินจึงได้รับโจทย์ใหม่ ในการบอกข้อสอบสุดท้าทายครั้งนี้ โดยเธอตัดสินใจบินไปสอบที่ประเทศที่เร็วกว่าไทย เพื่อรีบนำมาบอกข้อสอบแก่เหล่าลูกค้าของเธอ โดยเธอต้องหาผู้ช่วยอย่าง แบงค์ (ชานน สันตินธรกุล) นักเรียนทุนคู่แข่ง ที่ไม่ชอบการทุจริตใดๆ ในขณะที่เดิมพันเงินรางวัลที่สูงมากขึ้น ความยากในการโกงข้อสอบและความปลอดภัยของพวกเธอก็สูงขึ้นเช่นกัน</p>
